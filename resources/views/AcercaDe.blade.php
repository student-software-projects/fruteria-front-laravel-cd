<P> MISIÓN </p>
Distribución de frutas y verduras de calidad para que lleguen en el menor tiempo posible desde el productor hasta el consumidor final.

Mejorar los hábitos alimenticios, favoreciendo el consumo diario de frutas y verduras necesario para conseguir una dieta saludable.

Crear valor y marcar la diferencia en honradez, calidad y buen servicio.

<P> VISION </p>
Personas: Ser un buen lugar donde trabajar, que las personas se sientan motivadas para dar cada día lo mejor de sí mismas.

Servicio: ser una organización de referencia por el buen trato y servicio dado a sus clientes. Preocupación real por las personas que atendemos.

Productividad: Ser una organización eficaz y dinámica.

Sociedad: Ayudar a las personas a mejorar su calidad de vida, mejorando su dieta alimenticia.

<P> VALORES </P>

Calidad: búsqueda de la excelencia.

Diversidad: contar con un amplio abanico de productos.

Honradez: La ética empresarial marca todo nuestro comportamiento.

Colaboración: potenciar el talento colectivo.

Pasión: estar comprometidos con nuestros proveedores y clientes.

